<?php
// Menggunakan echo dengan beberapa argumen
echo "Hello, ", "world!", "\n";

// Menggunakan echo dengan satu argumen
echo "Hello, world!\n";

// Menggunakan print
print "Hello, world!\n";

// Menggunakan print dalam ekspresi
$success = print "Hello, world!\n";
if ($success) {
    echo "Print berhasil!\n";
}
?>